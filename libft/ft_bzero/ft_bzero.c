#include <stdio.h>

void	ft_bzero(void *s, size_t n)
{
	char			*str;
	unsigned int	i;

	i = 0;
	str = (char *)s;
	while ((str[i] != '\0') && (i < n))
	{
		str[i] = 0;
		i++;
	}
}
